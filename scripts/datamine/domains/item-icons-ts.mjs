import fs from 'node:fs';
import path from 'node:path';
import { loadTable, enumTail } from '../lib/tables.mjs';
import {
  resolveUIMaterial,
  resolveTagIcon,
  assetPathToIconFile,
} from '../lib/resolvers.mjs';
import { copyIcon, iconBasenameFromAsset } from '../lib/icons.mjs';
import { REPO_ROOT } from '../lib/config.mjs';

function iconFor(row, detailInfo) {
  let asset = resolveUIMaterial('UIMaterialsItem', row.UIMaterialID);
  if (!asset) asset = resolveTagIcon(detailInfo?.MaterialTag_Normal);
  if (!asset) return null;
  const src = assetPathToIconFile(asset);
  if (!src) return null;
  const fileName = iconBasenameFromAsset(asset);
  if (!fileName) return null;
  return copyIcon(src, 'items', fileName);
}

export function buildItemIconsTs() {
  const items = loadTable('Item');
  const detailTypes = loadTable('ItemDetailTypeInfo');

  const map = {};
  for (const row of Object.values(items)) {
    const detail = detailTypes[enumTail(row.ItemDetailType)];
    const url = iconFor(row, detail);
    if (url) map[row.ItemId] = url;
  }

  const outFile = path.join(REPO_ROOT, 'src', 'data', 'item-icons.ts');
  const entries = Object.entries(map)
    .sort((a, b) => Number(a[0]) - Number(b[0]))
    .map(([id, url]) => `  ${id}: "${url}"`)
    .join(',\n');
  const contents =
    `// AUTO-GENERATED by scripts/datamine — do not edit\n` +
    `// Maps ItemId → icon URL (public path)\n\n` +
    `export const ITEM_ICONS: Record<number, string> = {\n${entries}\n};\n\n` +
    `export function getItemIcon(id: number): string | null {\n` +
    `  return ITEM_ICONS[id] ?? null;\n` +
    `}\n`;
  fs.writeFileSync(outFile, contents);
  return { count: Object.keys(map).length, outFile };
}
